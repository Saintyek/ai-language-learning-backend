/**
 * 用户模块
 * 负责处理用户相关的所有业务逻辑
 */
@Module({
  imports: [TypeOrmModule.forFeature([User])],
  providers: [UserService],
  controllers: [UserController],
  exports: [UserService],
})
export class UserModule {}
